public class Day3_OperatorsLab2AssignShort {
    public static void main(String[] args) {
        // create variables
        int a = 5;
        int b;

        // assign value using =
        b = a;
        System.out.println("b using =: " + b);

        // assign value using =+
        b += a;
        System.out.println("b using +=: " + b);

        // assign value using =*
        b *= a;
        System.out.println("b using *=: " + b);
    }
}
