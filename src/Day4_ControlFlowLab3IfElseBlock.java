/**
 * Program to find if score Grade is "PASS" or "DETAINED"
 */
public class Day4_ControlFlowLab3IfElseBlock {
    public static void main(String[] args) {
        System.out.println("Find Grade");
        int input = 95;
        if (input >= 35) {
            System.out.println("PASS");
        } else {
            System.out.println("DETAINED");
        }
    }
}
